const questionsContainer = document.getElementById('questions-container');
const verifyAnswersButton = document.getElementById('verify-answers');
const resultsDiv = document.getElementById('results');
let currentQuestionIndex = 0;

// Función para generar las preguntas y opciones de respuesta
function showQuestions(startIndex, endIndex) {
    for (let i = startIndex; i < endIndex; i++) {
        const question = jsonData.preguntas[i];
        const questionDiv = document.createElement('div');
        questionDiv.innerHTML = `
            <h2>${question.enunciado}</h2>
            ${question.respuestas.map((option, j) => `
                <input type="radio" name="question${i}" id="option${j}" value="${option.opcion}">
                <label for="option${j}">${option.opcion}</label>
            `).join('')}
        `;
        questionsContainer.appendChild(questionDiv);
    }
}


function verifyAnswers() {
    let score = 0;

    for (let i = currentQuestionIndex; i < currentQuestionIndex + 3; i++) {
        const question = jsonData.preguntas[i];
        const userAnswer = document.querySelector(`input[name="question${i}"]:checked`)?.value;
        if (userAnswer === question.respuestas.find(option => option.correcta).opcion) {
            score++;
        }
    }

    resultsDiv.textContent = `Tu puntuación es: ${score}/3`;

    
    if (score === 3) {
        currentQuestionIndex += 3;
        questionsContainer.innerHTML = '';
        if (currentQuestionIndex < jsonData.preguntas.length) {
            showQuestions(currentQuestionIndex, currentQuestionIndex + 3);
        } else {
            resultsDiv.textContent = '¡Felicidades! Has respondido todas las preguntas correctamente.';
        }
    }
}

showQuestions(currentQuestionIndex, currentQuestionIndex + 3);

verifyAnswersButton.addEventListener('click', verifyAnswers); 
 









